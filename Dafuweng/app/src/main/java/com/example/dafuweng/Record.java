package com.example.dafuweng;

public class Record {

    String number;
    String user_name1;
    String user_name2;
    int money;
    int mode;
    String datetime;

    public Record(String number, String user_name1, String user_name2, int money, int mode, String datetime) {
        this.number = number;
        this.user_name1 = user_name1;
        this.user_name2 = user_name2;
        this.money = money;
        this.mode = mode;
        this.datetime=datetime;
    }

}
